package net.jiyuu_ni.seiidex.dto.csv;

public class ContestEffects {
		private int id;
		private int appeal;
		private int jam;
		
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public int getAppeal() {
			return appeal;
		}
		public void setAppeal(int appeal) {
			this.appeal = appeal;
		}
		public int getJam() {
			return jam;
		}
		public void setJam(int jam) {
			this.jam = jam;
		}
}
